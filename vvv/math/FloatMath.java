package vvv.math;

/**
 *
 * @author Вячеслав
 */
public class FloatMath 
{
    public static float sin( float x) 
    {
        return (float)Math.sin( x ); 
    }
    
    public static float cos( float x)
    {
        return (float)Math.cos(x);
    }
    
    public static float sqrt( float x)
    {
        return (float)Math.sqrt(x);
    }
    
}
